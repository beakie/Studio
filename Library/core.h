#ifndef CORE_H
#define CORE_H

/// <summary>
/// Boolean (bool)
/// </summary>
typedef bool UInt1;
/// <summary>
/// 1 byte unsigned integer (unsigned char)
/// </summary>
typedef unsigned char UInt8;
/// <summary>
/// 2 byte unsigned integer (unsigned short)
/// </summary>
typedef unsigned short UInt16;
/// <summary>
/// 4 byte unsigned integer (unsigned int)
/// </summary>
typedef unsigned int UInt32;
/// <summary>
/// 1 byte signed integer (signed char)
/// </summary>
typedef signed char Int8;
/// <summary>
/// 2 byte signed integer (signed short)
/// </summary>
typedef signed short Int16;
/// <summary>
/// 4 byte signed integer (signed int)
/// </summary>
typedef signed int Int32;
/// <summary>
/// Floating point numeric (float)
/// </summary>
typedef float Float32;
/// <summary>
/// Floating point numeric (double)
/// </summary>
typedef double Float64;
/// <summary>
/// Max available size unsigned integer
/// </summary>
typedef UInt32 UIntMax;
/// <summary>
/// Max available size integer
/// </summary>
typedef Int32 IntMax;
/// <summary>
/// Max available size floating point
/// </summary>
typedef Float64 FloatMax;

#endif // CORE_H
